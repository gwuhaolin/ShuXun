/**
 * 对13位isbn编码求其最后一位校验码
 * @param isbn12
 * @return String 返回校验码
 */
exports.checkCode13 = function (isbn12) {
    var s = 0;
    for (var i = 0; i < 12; i++) {
        var num = parseInt(isbn12.charAt(i));
        if (i % 2 == 0) {
            s += num;
        } else {
            s += num * 3;
        }
    }
    var m = s % 10;
    var n = 10 - m;
    var check = String(n);
    if (n == 10) {
        check = '0';
    }
    return check;
};

exports.genISBN13ByPublisher = function (publisherCode) {
    /**
     * 生成指定长度的0组成的字符串
     * @param length 0的长度
     * @returns {string}
     */
    function gen0(length) {
        var re = '';
        for (var i = 0; i < length; i++) {
            re += 0;
        }
        return re;
    }

    var start = '9787';//978图书 7中国
    start += publisherCode;
    var blankLength = 12 - start.length;
    var max = Math.pow(10, blankLength);
    var re = [];
    for (var i = 0; i < max; i++) {
        var strI = String(i);
        var one = start + gen0(blankLength - strI.length) + strI;
        one += exports.checkCode13(one);
        re.push(one);
    }
    return re;
};
